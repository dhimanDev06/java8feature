public class EncapStudent {
    private  int id;
    private String name;

    public EncapStudent(int i, String n){
        this.id = i;
        this.name = n;
    }
    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}

